    public DefaultTableModel TraerTabla(ResultSet rs) {
        DefaultTableModel model = new DefaultTableModel();
        try {
            for (int i = 0; i < rs.getColumnDefinitions().size(); i++) {
                model.addColumn(rs.getColumnDefinitions().getName(i));
            }
            rs.forEach(rr -> {
                Object[] row = new Object[rs.getColumnDefinitions().size()];
                for (int i = 0; i < rs.getColumnDefinitions().size(); i++) {
                    row[i] = rr.getObject(i);
                }
                model.addRow(row);
            }
            );
            return model;
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return model;
    }

    public void ActualizarCB() {
        ResultSet prof = db.Select("profesor");
        ResultSet alum = db.Select("alumno");
        ResultSet lice = db.Select("licencia");
        ResultSet vehi = db.Select("vehiculo");
        ArrayList<Object> idProf = new ArrayList();
        ArrayList<Object> idVehi = new ArrayList();

        DefaultComboBoxModel cbCulero = new DefaultComboBoxModel();
        DefaultComboBoxModel cbModVehi = new DefaultComboBoxModel();
        DefaultComboBoxModel cbModLice = new DefaultComboBoxModel();
        DefaultComboBoxModel cbModProf = new DefaultComboBoxModel();
        DefaultComboBoxModel cbModEstu = new DefaultComboBoxModel();
        try {
            //V E H I C U L O
            vehi.forEach(rr -> {
                cbModVehi.addElement(rr.getObject(0).toString());
                idVehi.add(rr.getObject(0).toString());
            }
            );
            cbElim_vehi.setModel(cbModVehi);

            //P R O F E S O R
            prof.forEach(rr -> {
                cbModProf.addElement(rr.getObject(0).toString());
                idProf.add(rr.getObject(0).toString());
            }
            );
            ArrayList<Object> Disponibles = getArrayListDifference(idProf, idVehi);
            for (Object Disponible : Disponibles) {
                cbCulero.addElement(Disponible);
            }
            cbProf_vehiculo.setModel(cbCulero);
            cbProf_estudiante.setModel(cbModProf);
            cbElim_prof.setModel(cbModProf);
            cbIdProf_Matricula.setModel(cbModProf);

            //A L U M N O
            alum.forEach(rr -> {
                cbModEstu.addElement(rr.getObject(0));
            }
            );
            cbElim_estu.setModel(cbModEstu);
            cbIdEstu_Matricula.setModel(cbModEstu);

            //L I C E N C I A
            lice.forEach(rr -> {
                cbModLice.addElement(rr.getObject(0));
            }
            );

            cbLic_estudiante.setModel(cbModLice);
            cbLic_vehiculo.setModel(cbModLice);
            cbElim_lice.setModel(cbModLice);

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    public ArrayList<Object> getArrayListDifference(ArrayList<Object> list1, ArrayList<Object> list2) {
        ArrayList<Object> difference = new ArrayList<>();

        for (Object element : list1) {
            if (!list2.contains(element)) {
                difference.add(element);
            }
        }

        return difference;
    }

